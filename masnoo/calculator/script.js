// get input elements
const regionInput = document.getElementById("region");
const activeInput = document.getElementById("active");
const newInput = document.getElementById("new");
const periodInput = document.getElementById("period");

// get price element
const priceElement = document.getElementById("price");

// add event listeners
regionInput.addEventListener("change", calculatePrice);
activeInput.addEventListener("change", calculatePrice);
newInput.addEventListener("change", calculatePrice);
periodInput.addEventListener("change", calculatePrice);

function calculatePrice() {
  const region = regionInput.value;
  const active = activeInput.value;
  const newAccount = newInput.value;
  const period = periodInput.value;

  const price = prices[region][active][newAccount][period];

  priceElement.innerText = "$" + price.toFixed(2);
}


       
